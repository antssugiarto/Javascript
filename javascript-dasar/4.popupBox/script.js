/*
Popup Box

1. Alert
    Popup box yang hanya menampilkan pemberitahuan.
    Struktur: alert(<pesan>)
    Contoh: alert("Hello, this alert");

2. Prompt
    Popup box yang menampilkan pemberitahuan dan memerintah user untuk melakukan input. Prompt mengembalikan suatu nilai, baiknya prompt diletakan di variable.
    Struktur: prompt(<pesan>)
    Contoh: prompt("Hello, this prompt, what your name?");

3. Confirm
    Popup box yang menampilkan pemberitahuan dan memberikan konfirmasi 'ya' atau 'tidak' pada user.
    Struktur: confirm(<pesan>)
    Contoh: confirm("Hello, this confirm, are you sure?");

*/