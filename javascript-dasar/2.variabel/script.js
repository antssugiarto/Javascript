/*
Variabel

Variabel adalah wadah untuk menampung nilai. Ada 2 tahap membuat variabel :
    1. Deklarasi & inisialisai (Deklarasi bisa menggunakan var, let, const)
    2. Assignment

Struktur :
    var [nama variabel]; (Deklarasi & inisialisai)
    [nama variabel] = [nilai]; (Assignment)

Aturan penamaan variabel :
    1. Tidak boleh diawal dengan angka, tapi boleh mengandung angka
    2. Tidak boleh menggunakan spasi

Contoh :
    var x; (Deklarasi & inisialisai)
    x = 20; (Assignment)

    atau

    var x = 20, y = 5;




*/