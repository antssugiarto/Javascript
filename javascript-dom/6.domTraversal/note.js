/*
DOM Traversal
    Note : - Untuk menelusuri node elemen pada HTML
           - Node itu semua karakter, element itu tag HTML

    Masuk ke elemen parent : .parentElement.
    Target elemen dalam function for : function(e) {e.target. ...}

    DOM Traversal Method :
        1. .parentNode. : Mengambil parent dari child node
        2. .parentElement. : Mengambil parent dari child elemen
        3. .nextSibling. : Mengambil node selanjutnya dalam parent yang sama
        4. .nextElementSibling. : Mengambil elemen selanjutnya dalam parent yang sama
        5. .previousSibling. : Mengambil node sebelumnya dalam parent yang sama
        5. .previousElementSibling. : Mengambil elemen sebelumnya dalam parent yang sama
*/